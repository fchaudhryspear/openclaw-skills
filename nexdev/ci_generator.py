def generate_ci_pipeline(project_dir: str, platform: str = "github") -> Dict[str, Any]:
    """
    Generate CI/CD pipeline configuration for a project.
    
    Args:
        project_dir: Root directory of the project
        platform: "github" or "gitlab"
        
    Returns:
        Dictionary with generated pipeline configuration
    """
    project_path = Path(project_dir)
    project_info = detect_project_type(project_dir)
    
    language = project_info['primary_language']
    
    result = {
        "success": False,
        "project_dir": project_dir,
        "detected_language": language,
        "platform": platform,
        "files_created": []
    }
    
    if platform.lower() == "github":
        if language == "python":
            content = generate_github_actions_python(project_info)
            workflow_file = project_path / ".github/workflows/ci.yml"
        elif language in ["javascript", "typescript"]:
            content = generate_github_actions_nodejs(project_info)
            workflow_file = project_path / ".github/workflows/ci.yml"
        else:
            result["message"] = f"No GitHub Actions template for {language}"
            return result
            
    elif platform.lower() == "gitlab":
        if language == "python":
            content = generate_gitlab_ci_python(project_info)
            workflow_file = project_path / ".gitlab-ci.yml"
        else:
            result["message"] = f"No GitLab CI template for {language}"
            return result
    else:
        result["message"] = f"Unsupported platform: {platform}"
        return result
    
    # Create directory and write file
    workflow_file.parent.mkdir(parents=True, exist_ok=True)
    workflow_file.write_text(content)
    
    result["success"] = True
    result["files_created"].append(str(workflow_file))
    result["message"] = f"Generated {platform.title()} CI pipeline for {language} project"
    
    return result


def add_complete_ci_setup(project_dir: str) -> Dict[str, Any]:
    """
    Add complete CI/CD setup including both GitHub and GitLab configs.
    
    Args:
        project_dir: Root directory of the project
        
    Returns:
        Summary of all configurations created
    """
    project_path = Path(project_dir)
    results = []
    
    # Generate for GitHub Actions
    gh_result = generate_ci_pipeline(project_dir, "github")
    results.append(gh_result)
    
    # Generate for GitLab CI
    gl_result = generate_ci_pipeline(project_dir, "gitlab")
    results.append(gl_result)
    
    return {
        "project_dir": project_dir,
        "summary": {
            "github_actions": gh_result.get("success", False),
            "gitlab_ci": gl_result.get("success", False),
            "total_files": len([r for r in results if r.get("success")])
        },
        "detailed_results": results
    }


