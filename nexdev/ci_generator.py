def detect_project_type(project_dir: str) -> Dict[str, Any]:
    """
    Detect project type from directory structure.
    
    Args:
        project_dir: Root directory of the project
        
    Returns:
        Dictionary with detected language, frameworks, and config files found
    """
    project_path = Path(project_dir)
    
    # Check for language indicators
    indicators = {
        "python": [
            "requirements.txt", "setup.py", "pyproject.toml", 
            "Pipfile", ".pylintrc", "manage.py"
        ],
        "javascript": [
            "package.json", "yarn.lock", "node_modules",
            ".eslintrc", ".babelrc"
        ],
        "typescript": [
            "tsconfig.json", "package.json"
        ],
        "java": [
            "pom.xml", "build.gradle", "settings.gradle", "mvnw"
        ],
        "go": [
            "go.mod", "go.sum"
        ],
        "ruby": [
            "Gemfile", "Rakefile", ".rspec"
        ]
    }
    
    detected_languages = []
    config_files = {}
    
    for lang, files in indicators.items():
        matches = []
        for f in files:
            if (project_path / f).exists():
                matches.append(f)
        
        if matches:
            detected_languages.append(lang)
            config_files[lang] = matches
    
    # Primary language is first match
    primary_language = detected_languages[0] if detected_languages else "unknown"
    
    return {
        "primary_language": primary_language,
        "all_languages": detected_languages,
        "config_files": config_files,
        "has_tests": _check_for_tests_directory(project_path),
        "framework": _detect_test_framework(project_path)
    }


def _check_for_tests_directory(project_path: Path) -> bool:
    """Check if project has a tests directory."""
    possible_names = ["tests", "test", "spec", "__tests__"]
    return any((project_path / name).is_dir() for name in possible_names)


def _detect_test_framework(project_path: Path) -> str:
    """Detect the test framework being used."""
    # Python
    if (project_path / "pytest.ini").exists():
        return "pytest"
    if (project_path / "tox.ini").exists():
        return "tox"
    
    # JavaScript/TypeScript
    if (project_path / "jest.config.js").exists():
        return "jest"
    if (project_path / ".mocharc.json").exists():
        return "mocha"
    
    # Java
    if (project_path / "pom.xml").exists():
        pom_content = (project_path / "pom.xml").read_text()
        if "junit" in pom_content:
            return "junit"
    
    # Ruby
    if (project_path / ".rspec").exists():
        return "rspec"
    
    return "unknown"


